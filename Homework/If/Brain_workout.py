



first_number = int(input("Введите первое число: "))
second_number = int(input("Введите второе число: "))
sum = int(input("Введите предполаемую сумму чисел: "))
summa = first_number + second_number
if sum == summa:
    print("Ответ верный!")
else:
    print("Ответ неверный!\nПотренируйтесь ещё!")